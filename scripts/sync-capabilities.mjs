#!/usr/bin/env node

import { createHash } from "node:crypto";
import { readFile, mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUTPUT_PATH = join(ROOT, "reference/protocol/capabilities.mdx");
const REPOSITORY = "TinyCloudLabs/tinycloud-node";
const LOCAL_REGISTRY = process.env.TINYCLOUD_CAPABILITIES_PATH;
const CHECK = process.argv.includes("--check");

async function loadRegistry() {
  if (LOCAL_REGISTRY) {
    const raw = await readFile(LOCAL_REGISTRY, "utf8");
    const result = spawnSync("git", ["-C", dirname(LOCAL_REGISTRY), "rev-parse", "HEAD"], {
      encoding: "utf8",
    });
    if (result.status !== 0) {
      throw new Error(`Could not resolve the local tinycloud-node commit: ${result.stderr}`);
    }
    return { raw, sourceGitSha: result.stdout.trim() };
  }

  const commitResponse = await fetch(`https://api.github.com/repos/${REPOSITORY}/commits/main`, {
    headers: {
      Accept: "application/vnd.github+json",
      ...(process.env.GITHUB_TOKEN ? { Authorization: `Bearer ${process.env.GITHUB_TOKEN}` } : {}),
    },
  });
  if (!commitResponse.ok) {
    throw new Error(`Could not resolve ${REPOSITORY}@main: ${commitResponse.status}`);
  }
  const { sha: sourceGitSha } = await commitResponse.json();

  const registryResponse = await fetch(
    `https://raw.githubusercontent.com/${REPOSITORY}/${sourceGitSha}/capabilities.json`,
  );
  if (!registryResponse.ok) {
    throw new Error(`Could not fetch capabilities.json at ${sourceGitSha}: ${registryResponse.status}`);
  }
  return { raw: await registryResponse.text(), sourceGitSha };
}

function validate(registry) {
  const statuses = new Set(["active", "deprecated-alias", "reserved"]);
  const seen = new Set();

  if (!Number.isInteger(registry.version) || !Array.isArray(registry.capabilities)) {
    throw new Error("Invalid capability registry: expected version and capabilities");
  }

  for (const capability of registry.capabilities) {
    if (!capability.urn?.startsWith(`${capability.service}/`)) {
      throw new Error(`Capability ${capability.urn} does not belong to ${capability.service}`);
    }
    if (!statuses.has(capability.status)) {
      throw new Error(`Capability ${capability.urn} has unknown status ${capability.status}`);
    }
    if (seen.has(capability.urn)) {
      throw new Error(`Duplicate capability ${capability.urn}`);
    }
    if (capability.status === "deprecated-alias" && !capability.aliasOf) {
      throw new Error(`Deprecated alias ${capability.urn} has no aliasOf target`);
    }
    seen.add(capability.urn);
  }
}

function relationship(capability) {
  if (capability.aliasOf) return `Alias of \`${capability.aliasOf}\``;
  if (capability.implies?.length) {
    return `Implies ${capability.implies.map((urn) => `\`${urn}\``).join(", ")}`;
  }
  return "—";
}

function notes(capability) {
  return capability.notes
    ? capability.notes
        .replaceAll("&", "&amp;")
        .replaceAll("|", "\\|")
        .replaceAll("{", "&#123;")
        .replaceAll("}", "&#125;")
        .replaceAll(/\s+/g, " ")
    : "—";
}

function render({ registry, sourceGitSha, sourceHash }) {
  const grouped = new Map();
  const counts = new Map();
  for (const capability of registry.capabilities) {
    grouped.set(capability.service, [...(grouped.get(capability.service) ?? []), capability]);
    counts.set(capability.status, (counts.get(capability.status) ?? 0) + 1);
  }
  const sourceUrl = `https://github.com/${REPOSITORY}/blob/${sourceGitSha}/capabilities.json`;
  const lines = [
    "---",
    'title: "Capability Registry"',
    'description: "Capability action URNs and status from the canonical TinyCloud node registry"',
    'icon: "list-tree"',
    "---",
    "",
    "{/* Generated by scripts/sync-capabilities.mjs. Do not edit by hand. */}",
    "",
    "This page is the source-attributed implementation reference for capability action names and status in the canonical `tinycloud-node` registry. For the authorization model behind these names, see [Capabilities](/concepts/capabilities).",
    "",
    "<Info>",
    `  Synced from canonical registry version ${registry.version} at [\`${sourceGitSha.slice(0, 7)}\`](${sourceUrl}) (source SHA-256 \`${sourceHash}\`). The registry contains ${counts.get("active") ?? 0} active actions, ${counts.get("deprecated-alias") ?? 0} deprecated aliases, and ${counts.get("reserved") ?? 0} reserved actions.`,
    "</Info>",
    "",
    "## Ability syntax",
    "",
    "An ability combines a service namespace and an action:",
    "",
    "```text",
    "tinycloud.{service}/{action}",
    "```",
    "",
    "The ability states **what** a delegate may do. The resource URI and capability caveats state **where** and under which constraints it may do it.",
    "",
    "## Statuses",
    "",
    "| Status | Meaning |",
    "| --- | --- |",
    "| Active | Accepted as an active protocol action. SDK availability and runtime effects vary by service. |",
    "| Deprecated alias | Accepted for compatibility and normalized to the listed canonical action. Use the canonical action in new integrations. |",
    "| Reserved | Recognized at the policy boundary but not dispatched to a service. Do not request reserved actions. |",
    "",
    "<Warning>",
    "  Registry inclusion describes the node's authorization boundary. It does not by itself guarantee a high-level SDK method, a dedicated side effect, or availability on every deployment. Treat this registry—not proposal-only whitepaper names—as the source of truth for action spelling, relationships, and status.",
    "</Warning>",
    "",
  ];

  for (const [service, capabilities] of [...grouped.entries()].sort(([a], [b]) => a.localeCompare(b))) {
    lines.push(
      `## \`${service}\``,
      "",
      "| Ability | Status | Relationship | Registry note |",
      "| --- | --- | --- | --- |",
    );
    for (const capability of [...capabilities].sort((a, b) => a.urn.localeCompare(b.urn))) {
      lines.push(
        `| \`${capability.urn}\` | ${capability.status} | ${relationship(capability)} | ${notes(capability)} |`,
      );
    }
    lines.push("");
  }

  lines.push(
    "## Related documentation",
    "",
    "- [Capabilities](/concepts/capabilities) explains delegation, attenuation, and validation.",
    "- [Delegations & Sharing](/guides/delegations) shows how to grant and use capabilities.",
    "- [App manifests](/concepts/app-manifests) describes how applications request capabilities during sign-in.",
    "",
  );
  return lines.join("\n");
}

const { raw, sourceGitSha } = await loadRegistry();
const registry = JSON.parse(raw);
validate(registry);
const sourceHash = createHash("sha256").update(raw).digest("hex");
const output = render({ registry, sourceGitSha, sourceHash });

if (CHECK) {
  const current = await readFile(OUTPUT_PATH, "utf8").catch(() => "");
  if (current !== output) {
    console.error("Capability reference is stale. Run `npm run sync-capabilities`.");
    process.exit(1);
  }
  console.log("Capability reference is up to date.");
} else {
  await mkdir(dirname(OUTPUT_PATH), { recursive: true });
  await writeFile(OUTPUT_PATH, output);
  console.log(`Wrote ${OUTPUT_PATH}`);
}
